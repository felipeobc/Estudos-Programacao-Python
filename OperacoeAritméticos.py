numero01 = int(input('Digite um nuemro: '))
numero02 = int(input('Digite um outro nuemro: '))

soma = numero01 + numero02
subtracao = numero01 - numero02
produto = numero01 * numero02
divisao = numero01 / numero02
exponecial = numero01 ** numero02

print('A soma é: {}\nA Subtração é: {}\nO Produto é: {}\nA Divisao é: {:.2f}\nO Exponecial é: {:.2f}'.format(soma, subtracao, produto, divisao, exponecial))
