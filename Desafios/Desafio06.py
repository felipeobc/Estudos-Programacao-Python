import math

numero = int(input('Digite um numero: '))

print('O numero digitado foi: {}'.format(numero))
print('O dobro desse numero é: {}' .format(numero * 2))
print('O triblo desse numero é: {}' .format(numero * 3))
print('A raiz quadrada desse numero é: {:.2f}' .format(math.sqrt(numero)))